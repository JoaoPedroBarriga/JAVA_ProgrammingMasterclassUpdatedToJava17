package Classes_032_Through_039;

public class _037_AssignmentOperatorVsEqualsToOperator {

    public static void main(String[] args) {

        //Equals To Operator: ==
        int newValue = 50;
        if (newValue == 50) {
            System.out.println("This is true");
        }

        //Assignment Operator: =
        boolean isCar = false;
        if (!isCar) {
            System.out.println("This is not supposed to happen");
        }

    }
}
